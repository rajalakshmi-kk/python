a='self'
b='love'
# str concatenation
print(a+ ' ' +b)

# str multiplication
print(b * 3)

# string interpolation
print("always be happy with your %s %s itself !"%(a,b))

# another method for str interpolation
print(f'always be happy with your {a} {b} itself !')

# Str formatting
name='Raji'
print('Hello, {}'.format(name))

